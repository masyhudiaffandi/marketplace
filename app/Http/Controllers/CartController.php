     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $Photo = null;

        if ($request->hasFile('photo')) {
            $Photo = $request->file('photo')->store('public/photo');
        }

        Cart::create([
            'name' => $request->name,
            'price' => $request->price,
            'desc' => $request->desc,
            'photo' => $Photo,
            'shipping_id' => $request->shipping_id,
        ]);

    }

